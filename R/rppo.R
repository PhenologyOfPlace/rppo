#' in the data portal are aligned using the Plant Phenology Ontology, available
#' at \url{https://github.com/PlantPhenoOntology/ppo}.
#' You may also view data portal products online at
#' \url{http://plantphenology.org/}.
#'
#' Two functions  are contained in the \pkg{rppo}:
#' \code{\link{ppo_terms}} allows users to discover present and absent
#' phenological stages while \code{\link{ppo_data}} enables
#' users to query the PPO data portal The \pkg{rppo} package source code is available
#' at \url{https://github.com/biococellc/rppo}.
